package main

func passcode(s string) (res []byte) {
	sequence := load_key()
	res = make([]byte, 16)
	for i := 0; i < len(s) && i < len(res); i++ {
		p := int(s[i]) + i
		if p > 126 {
			p = 32 + p%127
		}
		res[i] = sequence[p]
	}
	return res
}

